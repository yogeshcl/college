		<h3>Gurukula</h3>
		<p>Established in 2000, the Gurukula at Suttur Srikeshetra offers residential education facilities to the deserving disciples. Discourses on ‘Agama (shastras)’ and Veda are held at the Gurukula for disciples and ‘Sadhakas (Practitioners).’ The learning of Veda and ‘Agama’ in the early morning is followed by classroom studies. The students also partake in the religions camps that are periodically held in Suttur.</p>
        <p>After completing the education in Suttur, many students pursue higher studies in Mysore and other cities. While some become pontiffs of different maths, some engage themselves in religious activities of the society.</p>
        <h3>Retreat Camp for Legislators</h3>
        <p>In September 2008, Suttur Srikshetra successfully hosted for the second time a camp for legislators. The Math organized a three day camp for the members of the Legislative Assembly and Legislative Council a rare political-cum-spiritual retreat to inculcate in them moral values and social responsibilities. The camp was attended by more than 65 legislators cutting across party lines. The sessions were conducted by a battery of learned spiritual leaders and scholars from different backgrounds. The ‘retreat,’ besides discourses by eminent personalities on topics ranging from ‘nation building’ to ‘role of religious institutions in rural development’, also had sessions on yoga and meditations.</p>

