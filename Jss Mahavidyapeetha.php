		<p><b>Sri Sutturu Veerasimhasana Mahasamsthana Math</b>, vibrantly known as Sutturu Math founded 1,000 years ago by Sri Adi Jagadguru Shivarathreeshwara Shivayogigalu. Community life in India and in particular Karnataka is closely linked with spiritual and religious institutions. The Rishis have been a guiding force in upholding the moral values of life and in the progression of society as a whole. The glorious Suttur Math is a reflection and testimony to this rich tradition. Sri Jagadguru Veerasimhasana Suttur Math, with a history of more than ten centuries is a multi-dimensional institution of the Veerasaiva community that has contributed immensely to the cause of advancement of society. The welfare activities of the Math transcend the sectarian limits of the Veerasaiva community and encompass a much wider and larger following of all the sections of Indian society. Sri Suttur Veerasimhasana Math can aptly be described as an active ongoing movement to uphold the cause of social and economic justice. No wonder today, the Math’s activities have spread far beyond Suttur, a small region on the banks of the river Kapila in Karnataka to touch the lives of millions around and beyond the boundaries of the nation. With its administrative head-quarters located at the foot of the picturesque Chamundi Hills, Mysore, Sri Suttur Veerasimhasana Math is today recognized as a major mission, and the activities of the Math have drawn accolades from various quarters. The 23rd pontiff Rajaguru Tilaka Dr. Sri Shivarathri Rajendra Mahaswamigalu with the utmost sincerity established JSS Mahavidyapeetha to spread the tentacles of education through the length and breadth of the country. He is a proponent to say “Ignorance is the root of all evils in the society and education is the sovereign remedy”. </p>
		  <br>
		  <p>Jagadguru Sri Shivarathreeshwara Mahavidyapeetha (JSS MVP), is founded in 1954 by the 23rd pontiff, Jagadguru Dr. Sri Shivarathri Rajendra Mahaswamiji as an off shoot of Sri Sutturu Math to spread education among the rural people and society in large. What began in 1928, as a small hostel for poor students pursuing higher studies in Mysore, has today grown to be a gigantic educational movement. JSS Mahavidyapeetha never had a moment to look back and proceeded on a spree of establishing more than 320 institutions across the country and overseas. Spear heading the Maths education initiatives, JSS Mahavidyapeetha has established a Medical College, Four Engineering Colleges including SJCE at Mysore, JSS Academy of Technical Education at Bangalore, NOIDA and in Mauritius, apart from Colleges in Law & Pharmacy, Polytechnics, public schools, regular schools and colleges.</p>
